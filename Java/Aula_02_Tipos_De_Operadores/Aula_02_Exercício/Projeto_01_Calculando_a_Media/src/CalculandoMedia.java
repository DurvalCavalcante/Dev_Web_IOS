public class CalculandoMedia {
    // método main inicia a execução do aplicativo
    public static void main(String[] args) {
        System.out.println("Resultado do calculo da media das notas");
        System.out.println("Primeira Nota: " + 8);
        System.out.println("Segunda Nota: " + 9);
        System.out.println("Terceira Nota: " + 7);
        System.out.println("Quarta Nota: " + 5);
        System.out.println("Resultado do calculo: " + (8 + 9 + 7 + 5) / 4);
    } // fim do método main
}  // fim da classe