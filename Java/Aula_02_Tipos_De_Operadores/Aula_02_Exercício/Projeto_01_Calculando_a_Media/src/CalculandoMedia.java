public class CalculandoMedia {
    // método main inicia a execução do aplicativo
    public static void main(String[] args) {
        double nota1 = 8.0;
        double nota2 = 9.0;
        double nota3 = 7.0;
        double nota4 = 6.0;

        System.out.println("Resultado do calculo da media das notas");
        System.out.println("Primeira Nota: " + nota1);
        System.out.println("Segunda Nota: " + nota2);
        System.out.println("Terceira Nota: " + nota3);
        System.out.println("Quarta Nota: " + nota4);
        System.out.println("Resultado do calculo: " + (nota1 + nota2 + nota3 + nota4) / 4);
    } // fim do método main
}  // fim da classe